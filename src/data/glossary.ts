/** マッチ用：term + aliases を長い順に並べたインデックス */
export interface MatchEntry {
  word: string;
  id: string;
}
export const MATCH_INDEX: MatchEntry[] = GLOSSARY.flatMap((t) => [
  { word: t.term, id: t.id },
  ...(t.aliases ?? []).map((a) => ({ word: a, id: t.id })),
]).sort((a, b) => b.word.length - a.word.length);

export const TERM_BY_ID: Record<string, GlossaryTerm> = Object.fromEntries(
  GLOSSARY.map((t) => [t.id, t]),
);
